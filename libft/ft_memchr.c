#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	while (n--)
	{
		if ((unsigned char)c == *tmp)
			return (tmp);
		tmp++;
	}
	return (NULL);
}
